import React from 'react';
import { enquireScreen } from 'enquire-js';

import Content11 from './Content11';
import Feature5 from './Feature5';
import Content9 from './Content9';
import Teams0 from './Teams0';
import Footer2 from './Footer2';

import {
  Content110DataSource,
  Feature50DataSource,
  Content90DataSource,
  Teams00DataSource,
  Footer20DataSource,
} from './data.source';
// import '../../static/achieve/antMotionStyle.less';
import "../../static/achieveCSS/common.css";
import "../../static/achieveCSS/custom.css";
import "../../static/achieveCSS/content.css";
import "../../static/achieveCSS/content11.css";
import "../../static/achieveCSS/feature5.css";
import "../../static/achieveCSS/content9.css";
import "../../static/achieveCSS/teams0.css";
import "../../static/achieveCSS/footer2.css";
import "../../static/achieveCSS/edit.css";


let isMobile;
enquireScreen((b) => {
  isMobile = b;
});

const { location = {} } = typeof window !== 'undefined' ? window : {};

export default class Home extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      isMobile,
      show: !location.port, // 如果不是 dva 2.0 请删除
    };
  }

  componentDidMount() {
    // 适配手机屏幕;
    enquireScreen((b) => {
      this.setState({ isMobile: !!b });
    });
    // dva 2.0 样式在组件渲染之后动态加载，导致滚动组件不生效；线上不影响；
    /* 如果不是 dva 2.0 请删除 start */
    if (location.port) {
      // 样式 build 时间在 200-300ms 之间;
      setTimeout(() => {
        this.setState({
          show: true,
        });
      }, 500);
    }
    /* 如果不是 dva 2.0 请删除 end */
  }

  render() {
    const children = [
      <Content11
        id="Content11_0"
        key="Content11_0"
        dataSource={Content110DataSource}
        isMobile={this.state.isMobile}
      />,
      <Feature5
        id="Feature5_0"
        key="Feature5_0"
        dataSource={Feature50DataSource}
        isMobile={this.state.isMobile}
      />,
      <Content9
        id="Content9_0"
        key="Content9_0"
        dataSource={Content90DataSource}
        isMobile={this.state.isMobile}
      />,
      <Teams0
        id="Teams0_0"
        key="Teams0_0"
        dataSource={Teams00DataSource}
        isMobile={this.state.isMobile}
      />,
      <Footer2
        id="Footer2_0"
        key="Footer2_0"
        dataSource={Footer20DataSource}
        isMobile={this.state.isMobile}
      />,
    ];
    return (
      <div
        className="templates-wrapper"
        ref={(d) => {
          this.dom = d;
        }}
      >

        {/* 如果不是 dva 2.0 替换成 {children} start */}
        {this.state.show && children}
        {/* 如果不是 dva 2.0 替换成 {children} end */}
      </div>
    );
  }
}
